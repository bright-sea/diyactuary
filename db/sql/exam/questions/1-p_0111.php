<p>Auto claim amounts, in thousands, are modeled by a random variable with density function f (x) = x\(e^{-x}\) for x \(\geq\) 0 .</p>
<p>The company expects to pay 100 claims if there is no deductible.</p>
<p>How many claims does the company expect to pay if the company decides to introduce a deductible of 1000 ?</p>
</div>
<div class="QuestionOptions">
<input type="radio" name="questionOption" value="1" /><span class="QuestionOptionSign">(A)</span>26
<input type="radio" name="questionOption" value="2" /><span class="QuestionOptionSign">(B)</span>37
<input type="radio" name="questionOption" value="3" /><span class="QuestionOptionSign">(C)</span>50
<input type="radio" name="questionOption" value="4" /><span class="QuestionOptionSign">(D)</span>63
<input type="radio" name="questionOption" value="5" /><span class="QuestionOptionSign">(E)</span>74
</div>

<div class="QuestionSolution">
<p>If X is the random variable representing claim amounts, the probability that X exceeds the
deductible is
\[ \begin{array}{rcl} 
	Pr[X>1] & = & \int_1^\infty xe^{-x}dx  \\
	& = & xe^{-x}|_1^\infty + \int_1^\infty e^{-x}dx \text{ (integration by parts) }\\
	& = & e^{-1}-e^{-x}|_1^\infty \\
	& = & e^{-1}+e^{-1} \\
	& = & 2e^{-1} \\
	& = & 0.736
 \end{array} \]</p>
<p>It follows that the company expects (100)(0.736) = 74 claims.</p>
</div>
</div>




